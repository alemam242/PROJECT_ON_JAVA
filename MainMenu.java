    private void Show() {
        String sql = "Select name,email from user_info where username=?";
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, uname);
            rs = pst.executeQuery();
            if (rs.next()) {
                nm = rs.getString(1);
                email = rs.getString(2);

            } else {
                JOptionPane.showMessageDialog(null, "Username doesn't match");
            }
            con.close();
            BuyTicket.Nameis(nm, email);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

