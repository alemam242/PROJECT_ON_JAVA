    private void deleteaccountbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteaccountbtnActionPerformed
        int p = JOptionPane.showConfirmDialog(null, "Are you sure want to delete your account ?", "Delete Account", JOptionPane.YES_NO_OPTION);
        if (p == 0) {
            con = AppSetting.ConnectDT();
            String sql = "Delete from user_info where username=?";
            try {
                pst = con.prepareStatement(sql);
                pst.setString(1, uname);
                pst.execute();

                JOptionPane.showMessageDialog(null, "Account Deleted");
                con.close();
                LoginPanel l = new LoginPanel();
                l.setVisible(true);
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_deleteaccountbtnActionPerformed

