    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed

        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("MM-dd-yyyy");
        LocalDateTime now = LocalDateTime.now();

        dt = ((JTextField) DATE.getDateEditor().getUiComponent()).getText();
        SimpleDateFormat datef = new SimpleDateFormat("MM-dd-yyyy");
        String dtt = (dtf.format(now));
        try {
            Date d1 = datef.parse(dt);
            Date d2 = datef.parse(dtt);
            if (d1.compareTo(d2) < 0) {
                JOptionPane.showMessageDialog(null, "Please select present or future dates..");
            } else {
                if (!fromtxt.getText().isEmpty() && !totxt.getText().isEmpty()) {
                    con = AppSetting.ConnectDT();
                    String sql = "SELECT route.from_where, route.to_where, route.fare, route.time,route.bus_code, bus_info.available_sit"
                            + " FROM route INNER JOIN bus_info ON route.bus_code = bus_info.bus_code WHERE from_where = ? AND to_where = ?";
                    try {
                        pst = con.prepareStatement(sql);
                        pst.setString(1, fromtxt.getText());
                        pst.setString(2, totxt.getText());
                        rs = pst.executeQuery();
                        datagridview.setModel(DbUtils.resultSetToTableModel(rs));
                        con.close();
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, ex);
                    }
                    dataviewpnl.setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Enter your terminal and destination");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Select present or future date");
        }
    }//GEN-LAST:event_searchbtnActionPerformed

