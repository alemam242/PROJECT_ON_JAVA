    private void Show() {
        con = AppSetting.ConnectDT();
        String sql = "Select name,username,email,gender from user_info where username=?";
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, uname);
            rs = pst.executeQuery();
            if (rs.next()) {
                nm = rs.getString(1);
                nametxt.setText(rs.getString(1));

                usernametxt.setText(rs.getString(2));
                unm = rs.getString(2);

                email = rs.getString(3);
                emailtxt.setText(rs.getString(3));

                gen = rs.getString(4);

                gend = gen;

                try {
                    if (gen.equalsIgnoreCase("Male")) {
                        imageName = "E:\\UI_DESIGN\\male.png";
                    } else {
                        imageName = "E:\\UI_DESIGN\\female.png";
                    }
                    pic.setIcon(new ImageIcon(ImageIO.read(new File(imageName))));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "File not found!!!");
                }
                if (cd == 0) {
                    nametxt.setEditable(false);
                    usernametxt.setEditable(false);
                    emailtxt.setEditable(false);
                    malebtn.setText(gen);
                } else if (cd == 1) {
                    nametxt.setEditable(true);
                    usernametxt.setEditable(true);
                    emailtxt.setEditable(true);
                    malebtn.setText("Male");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Username doesn't match");
            }
            con.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
