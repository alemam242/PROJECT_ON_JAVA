    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        if (nametxt.getText().isEmpty() && usernametxt.getText().isEmpty() && emailtxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Infromation is not valid!!");
        } else if (nametxt.getText().equalsIgnoreCase(nm) && usernametxt.getText().equalsIgnoreCase(unm) && emailtxt.getText().equalsIgnoreCase(email) && gend.equalsIgnoreCase(gen)) {
            JOptionPane.showMessageDialog(null, "Change your infromation to update");
        } else {
            con = AppSetting.ConnectDT();
            String v1 = nametxt.getText();
            String v2 = usernametxt.getText();
            String v3 = emailtxt.getText();

            String sql = "Update user_info set name=?,username=?,email=?,gender=? where username=?";

            try {
                pst = con.prepareStatement(sql);
                pst.setString(1, v1);
                pst.setString(2, v2);
                pst.setString(3, v3);
                pst.setString(4, gen);
                pst.setString(5, uname);
                pst.execute();

                JOptionPane.showMessageDialog(null, "Infromation Updated");
                con.close();
                
                MainMenu.uname = v2;
                uname=v2;
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_updatebtnActionPerformed

